package com.buildhawk.utils;

public class subcontractors {

	public String sId; 
	public String sName;
	public String sEmail;
	public String sPhone;
	public String sCount;
	
	
	public subcontractors(String id, String name,String email, String phone,String count){
		
		this.sId = id;		
		this.sName = name;
		this.sEmail = email;
		this.sPhone = phone;
		this.sCount = count;
	}
			
			
}

